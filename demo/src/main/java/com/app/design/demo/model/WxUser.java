package com.app.design.demo.model;

// 内部类或单独文件也可以
public class WxUser {
    private int id;
    private String openid;

    // Getter 和 Setter
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getOpenid() {
        return openid;
    }

    public void setOpenid(String openid) {
        this.openid = openid;
    }
}
